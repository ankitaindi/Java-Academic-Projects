    private void txtCarMakeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCarMakeKeyPressed
        // TODO add your handling code here:
        
        char c = evt.getKeyChar();
        if(Character.isDigit(c)){
            txtCarMake.setEditable(false);
            lblcarmodelvali.setText("Please Car Model only!");
        }
        else
        {
            txtCarMake.setEditable(true);
            lblcarmodelvali.setText(""); // To disable the warnings once user feed correct character
        }
    }//GEN-LAST:event_txtCarMakeKeyPressed

    private void txtCarModelKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCarModelKeyPressed
        // TODO add your handling code here:
        
        String CarModel=txtCarModel.getText();
        //get length of a string
        int length =CarModel.length();
        
        char c = evt.getKeyChar();
        
        //Check for number 0-9
        
        if(evt.getKeyChar()>='0' && evt.getKeyChar()<='4'){
            if (length<5){
                //editable true
                txtCarModel.setEditable(true);
                lblcarmodelnumvali.setText("Please enter Car Model  Number only!");
            }else{
                //not editable for length more than 10 digits 
                txtCarModel.setEditable(false);
                lblcarmodelnumvali.setText("");
                
            }
        }else{
            //allow  keys 'back space' and 'delete'
            if(evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE){
                //allow editable
                txtCarModel.setEditable(true);
                
            }else{
                txtCarModel.setEditable(false);
            }
                
            
           }
    }//GEN-LAST:event_txtCarModelKeyPressed

