    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        int year = Integer.parseInt(txtYear.getText());
        int numOfSeats = Integer.parseInt(txtNumSeats.getText());
        String RegDate  = txtRegDate.getText();
        String CarMake  = txtCarMake.getText();
        String ModelNum  = txtCarModel.getText();
        String SerialNum  = txtSerialNum.getText();
        String City  = txtCity.getText();
        Boolean Availability = chkBoxAvailable.getState();
        Boolean Expiry = chkBoxExpiry.getState();

        CarDetails cd = history.addNewCar();
        
        cd.setCarModel(CarMake);
        cd.setCarModelNumber(ModelNum);
        cd.setCarSerialNumber(SerialNum);
        cd.setNumOfSeats(numOfSeats);
        cd.setRegDate(RegDate);
        cd.setCity(City);

        cd.setYear(year);
        cd.setAvailability(Availability);
        cd.setExpiry(Expiry);
        
        JOptionPane.showMessageDialog(this, "New Car Detail is created");
        
        txtYear.setText("");
        txtNumSeats.setText("");
        txtCity.setText("");
        txtRegDate.setText("");
        txtCarMake.setText("");
        txtCarModel.setText("");
        txtSerialNum.setText("");
        chkBoxAvailable.setState(false);
        chkBoxExpiry.setState(false);

    }//GEN-LAST:event_btnSaveActionPerformed

